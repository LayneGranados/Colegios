    public void findItem(JComboBox combo, int id){
        boolean ya=false;
        for(int i=0;i<combo.getItemCount()&&!ya;i++){
            String c = (String)combo.getItemAt(i);
            if(c.startsWith(id+"-")){
                combo.setSelectedIndex(i);
                ya=true;
            }
        }
        
    }

