    private String[] crearColumnNamesListaCalificaciones(int idCurso){
        asignaturasMatricula = this.asignaturaBusiness.asignaturasPorCurso(idCurso);
        if(asignaturasMatricula != null && !asignaturasMatricula.isEmpty()){
            asignaturasMatricula.remove(0);
            String[] columnNames = new String[asignaturasMatricula.size()+2];
            columnNames[0] = "Código";
            columnNames[1] = "Nombres y Apellidos";
            for(int i=0;i<asignaturasMatricula.size();i++){
                columnNames[i+2] = asignaturasMatricula.get(i).getNombre();
            }
            return columnNames;
        }else{
            String[] columnNames = new String[2];
            columnNames[0] = "Código";
            columnNames[1] = "Nombres y Apellidos";
            return columnNames;
        }
    }
    
    private Object[][] dataTable(int curso, int tamColumnNames){
        matriculasCalificaciones  = this.matriculaBusiness.getEstudiantesMatriculadoEnCurso(curso);
        Object periodo = this.controller.getObjetoSeleccionado((GUI.Util.JComboBox)this.cmbPeriodoCalificaciones);
        Periodo per = (Periodo) periodo;
        if(per.getId()!=-1){
            this.notas = this.calificacionBusiness.buscarCalificacionDeAsignatura(curso, per.getId());
            int tamEstudiantes = matriculasCalificaciones.size();
            Object[][] data = new Object[tamEstudiantes][tamColumnNames];

            for(int i=0;i<matriculasCalificaciones.size();i++){
                Matricula m = matriculasCalificaciones.get(i);
                Estudiante e = m.getEstudiante();
                Persona p = e.getPersona();            
                data[i][0] = m.getCodigo();
                data[i][1] = p.getNombre1()+ " "+ p.getNombre2()+ " "+ p.getApellido1()+ " "+ p.getApellido2();
                for(int j=2;j<this.columnNames.length;j++){
                    data[i][j] = this.hallarNota(m.getCodigo(), this.columnNames[j]);
                }
            }
            return data;
        }
        return null;
    }
    
    private String hallarNota(String codigo, String asignatura){
        String nota = "-";
        for(int i=0;i<this.notas.size();i++){
            String fila = this.notas.get(i);
            String [] contenido = fila.split("/-/");
            if(contenido != null){
                if(contenido[0].equalsIgnoreCase(codigo) && contenido[1].equalsIgnoreCase(asignatura)){
                    nota = contenido[2];
                }
            }
        }
        return nota;
    }
    
    private Matricula hallarMatriculaPorCodigo(String codigo){
        for(Matricula m: this.matriculasCalificaciones){
            if(m.getCodigo().equalsIgnoreCase(codigo)){
                return m;
            }
        }
        return null;
    }
